package day1;

public class variables_and_datatypes {
        public static void main(String[] args) {
          // To print Variables and datatypes
          String name = "Ronit Jena"; //String Datatype
          int num = 10; //integer Datatype
          double digit = 10.24; // double datatype
          char a = 'a'; // Char datatype
          boolean check = true; // boolean datatype
      
          System.out.println(name);
          System.out.println(num);
          System.out.println(digit);
          System.out.println(a);
          System.out.println(check);
        }
}
